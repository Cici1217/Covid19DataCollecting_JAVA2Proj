public class DataFromWHO {
    private String Name;
    private String TotalCasesNum;
    private String NewlyReportedIn7DaysNum;
    private String TotalDeathNum;
    private String NewlyReportedDeathIn7DayNum;
    private String TotalVaccinePer100Population;
    private String FullyVaccinePer100Population;

    public void setName(String name) {
        Name = name;
    }

    public void setTotalCasesNum(String totalCasesNum) {
        TotalCasesNum = totalCasesNum;
    }

    public void setNewlyReportedIn7DaysNum(String newlyReportedIn7DaysNum) {
        NewlyReportedIn7DaysNum = newlyReportedIn7DaysNum;
    }

    public void setTotalDeathNum(String totalDeathNum) {
        TotalDeathNum = totalDeathNum;
    }

    public void setNewlyReportedDeathIn7DayNum(String newlyReportedDeathIn7DayNum) {
        NewlyReportedDeathIn7DayNum = newlyReportedDeathIn7DayNum;
    }

    public void setTotalVaccinePer100Population(String totalVaccinePer100Population) {
        TotalVaccinePer100Population = totalVaccinePer100Population;
    }

    public void setFullyVaccinePer100Population(String fullyVaccinePer100Population) {
        FullyVaccinePer100Population = fullyVaccinePer100Population;
    }
}
